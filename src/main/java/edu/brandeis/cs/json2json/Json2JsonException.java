package edu.brandeis.cs.json2json;


public class Json2JsonException extends Exception {

    public Json2JsonException(Throwable cause) {
        super(cause);
    }

    public Json2JsonException() {
        super();
    }



    public Json2JsonException(String message, Throwable cause) {
        super(message, cause);
    }

    public Json2JsonException(String s) {
        super(s);
    }

}